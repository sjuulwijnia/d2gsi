const ITEMS: { [dota2InternalName: string]: Item } = {};
items.forEach(item => {
	calculateItemNetworth(item);

	if (!!ITEMS[item.nameInternal]) {

		console.error(`ITEM ALREADY KNOWN: ${item.nameInternal}`);
		console.error(`CURRENT:`, ITEMS[item.nameInternal]);
		console.error(`NEW:`, item);
		throw new Error();
	}

	ITEMS[item.nameInternal] = Object.seal(item);
});

function calculateItemNetworth(item: Item): number {
	if ((item.networth === undefined || item.networth === null) && !item.networthPerCharge) {
		let networth = 0;

		item.recipeParts.forEach(recipeItem => {
			networth += calculateItemNetworth(recipeItem);
		});

		item.networth = networth;
	}

	return item.networth;
}

